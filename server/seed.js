async function seed() {
  console.log(`Добавляю ${courses.length} курсов...`)
  let added = 0
  for (const c of courses) {
    await prisma.course.create({ data: c })
    added++
    process.stdout.write(`\r  ${added}/${courses.length}`)
  }
  console.log(`\nГотово! Добавлено ${added} курсов.`)
}

seed()
  .catch(e => { console.error(e); process.exit(1) })
  .finally(() => prisma.$disconnect())
